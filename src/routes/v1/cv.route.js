const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const cvValidation = require('../../validations/cv.validation');
const cvController = require('../../controllers/cv.controller');

const router = express.Router();

router
  .route('/')
  .post(auth('manageCvs'), validate(cvValidation.createCv), cvController.createCv)
  .get(auth('getCvs'), validate(cvValidation.getCvs), cvController.getCvs);

router
  .route('/:cvId')
  .get(auth('getCvs'), validate(cvValidation.getCv), cvController.getCv)
  .patch(auth('manageCvs'), validate(cvValidation.updateCv), cvController.updateCv)
  .delete(auth('manageCvs'), validate(cvValidation.deleteCv), cvController.deleteCv);

module.exports = router;

